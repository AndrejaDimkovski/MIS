class Animal {
  final String name;
  final String info;
  final String image;
  final String order;
  final double width;
  final double weight;
  final String spread;
  final String habitat;

  Animal({
    required this.name,
    required this.info,
    required this.image,
    required this.order,
    required this.width,
    required this.weight,
    required this.spread,
    required this.habitat
  });
}
